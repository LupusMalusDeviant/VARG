        output.push_str("         panic!(\"[VargOS] Payload too short for AES-GCM.\");\n");
        output.push_str("    }\n");
        output.push_str("    let salt = &packed[0..16];\n");
        output.push_str("    let nonce_bytes = &packed[16..28];\n");
        output.push_str("    let ciphertext = &packed[28..];\n");
        output.push_str("    let derived_key = __varg_derive_key(key, salt);\n");
        output.push_str("    let cipher = Aes256Gcm::new_from_slice(&derived_key).unwrap();\n");
        output.push_str("    let nonce = Nonce::from_slice(nonce_bytes);\n");
        output.push_str("    let plaintext = cipher.decrypt(nonce, ciphertext).expect(\"[VargOS] Decryption failed or wrong password\");\n");
