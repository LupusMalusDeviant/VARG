        output.push_str("    OsRng.fill_bytes(&mut nonce_bytes);\n");
        output.push_str("    let nonce = Nonce::from_slice(&nonce_bytes);\n");
        output.push_str("    let ciphertext = cipher.encrypt(nonce, data.as_bytes()).expect(\"[VargOS] Encryption failed\");\n");
        
